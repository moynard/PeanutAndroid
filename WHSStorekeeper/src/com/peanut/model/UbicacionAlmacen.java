package com.peanut.model;

public class UbicacionAlmacen {
public String idUbicacion;
public String idUbicacionAlmacen;




public UbicacionAlmacen(String idUbicacion, String idUbicacionAlmacen) {
	super();
	this.idUbicacion = idUbicacion;
	this.idUbicacionAlmacen = idUbicacionAlmacen;
}
public String getIdUbicacion() {
	return idUbicacion;
}
public void setIdUbicacion(String idUbicacion) {
	this.idUbicacion = idUbicacion;
}
public String getIdUbicacionAlmacen() {
	return idUbicacionAlmacen;
}
public void setIdUbicacionAlmacen(String idUbicacionAlmacen) {
	this.idUbicacionAlmacen = idUbicacionAlmacen;
}





}
